    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if (txtPrimerNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu primer nombre");
            txtPrimerNombre.requestFocus();
            return;
        }
        if (txtSegundoNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu segundo nombre");
            txtSegundoNombre.requestFocus();
            return;
        }
        if (txtPrimerApellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu primer Apellido");
            txtPrimerApellido.requestFocus();
            return;
        }
        if (txtSegundoApellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu segundo apellido");
            txtSegundoApellido.requestFocus();
            return;
        }
        if (txtFechanac.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu Fecha de nacimiento");
            txtFechanac.requestFocus();
            return;
        }
        if (txtEdad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu edad");
            txtEdad.requestFocus();
            return;
        }
        if (txtDir.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu dirección de domicilio");
            txtDir.requestFocus();
            return;
        }
        if (txtTelf.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu número de teléfono");
            txtTelf.requestFocus();
            return;
        }
        if (txtemail.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu dirección de correo electrónico o Email");
            txtemail.requestFocus();
            return;
        }

        claCliente dts = new claCliente();
        fCliente func = new fCliente();

        dts.setPrimer_Nombre(txtPrimerNombre.getText());
        dts.setSegundo_Nombre(txtSegundoNombre.getText());

        dts.setPrimer_Apellido(txtPrimerApellido.getText());
        dts.setFecha_Nacimiento(txtFechanac.getText());

        dts.setEdad(Integer.parseInt(txtEdad.getText()));
        dts.setDirección_Domiciliaria(txtDir.getText());

        dts.setTeléfono(Double.parseDouble(txtTelf.getText()));
        dts.setEmail(txtemail.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El cliente se registró exitosamente");
                mostrar("");
                inhabilitar();
            }

        } else if (accion.equals("editar")) {
            dts.setIdCliente(Double.parseDouble(txtidCliente.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El cliente se editó exitosamente");
                mostrar("");
                inhabilitar();
            }

        }


    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidCliente.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar el cliente");
            if (confirmacion == 0) {
                fCliente func = new fCliente();
                claCliente dts = new claCliente();

                dts.setIdCliente(Double.parseDouble(txtidCliente.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }


    }//GEN-LAST:event_btneliminarActionPerformed

