        txtPrimerApellido.setText(tablalistacliente.getValueAt(fila, 3).toString());
        txtSegundoApellido.setText(tablalistacliente.getValueAt(fila, 4).toString());
        txtFechanac.setText(tablalistacliente.getValueAt(fila, 5).toString());
        txtEdad.setText(tablalistacliente.getValueAt(fila, 6).toString());
        txtDir.setText(tablalistacliente.getValueAt(fila, 7).toString());
        txtTelf.setText(tablalistacliente.getValueAt(fila, 8).toString());
